// JavaScript is a dynamic language.
// It means that we don't need to declare the type of variable. You don't have to specify types like int, string, or bool — JS figures it out at runtime.
// Variables can hold any type, and that type can change as the code runs — no compiler errors. This is called dynamic typing.

